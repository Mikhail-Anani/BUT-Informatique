package fr.iutfbleau.projetSAE2023.GroupeAlexisDjabrailMikhail;

/**
 * L'énumération Operateur définit un ensemble de constantes pour représenter
 * les opérateurs mathématiques de base.
 *
 * @version 1.0
 * @author  Alexis, Djabrail, Mikhail
 */

public enum Operateur{
    /**
     * L'opérateur de multiplication.
     */
    MULTIPLICATION,
    /**
     * L'opérateur de division.
     */
    DIVISION,
    /**
     * L'opérateur de soustraction.
     */
    SOUSTRACTION,
    /**
     * L'opérateur d'addition.
     */
    ADDITION
}